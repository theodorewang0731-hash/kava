#!/bin/bash
################################################################################
# 修复所有脚本执行权限
# 用法: bash fix_permissions_linux.sh
################################################################################

echo "正在修复所有 .sh 脚本的执行权限..."

# 查找并修复所有 .sh 文件
find . -type f -name "*.sh" -exec chmod +x {} \;

echo "✓ 权限修复完成！"
echo ""
echo "已修复的脚本："
ls -lh *.sh | awk '{print $1, $9}'
